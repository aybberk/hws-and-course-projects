#ifndef USER_H_INCLUDED
#define USER_H_INCLUDED
using namespace std;


class User
{
private:
    int userID;
    int userScore;
public:
///////////////////////////////////////////////////////////////////////////////////////////////
    User()
    {
        userScore=1500;
        userID=100;
    }

///////////////////////////////////////////////////////////////////////////////////////////////

    void SetScore(int score)
    {
        userScore=score;
    }

///////////////////////////////////////////////////////////////////////////////////////////////

    void AssignID(int d)
    {
        userID=d;
    }

///////////////////////////////////////////////////////////////////////////////////////////////

    int GetID()
    {
        return userID;
    }

///////////////////////////////////////////////////////////////////////////////////////////////

    int GetScore()
    {
        return userScore;
    }

///////////////////////////////////////////////////////////////////////////////////////////////

};

#endif
