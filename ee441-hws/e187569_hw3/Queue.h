
#ifndef QUEUE_H_INCLUDED
#define QUEUE_H_INCLUDED
#include "User.h"
#include <string>
#include <string.h>

class Queue         //this queue holds pointers for users
{
private:
    User *qList[2000];  //ilk(potansiyel) kisinin adresi
    User **qEnd;
    int currentSize;   //son kisinin bi arkasinin adresi
public:
    Queue()
    {
        for(int n=0;n<2000;n++)
        {
            qList[n]=NULL;
        }
        qEnd=qList;
        currentSize=0;
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    int GetCurrentSize()
    {
        return currentSize;
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    User* LookFront()
    {
        return *qList;
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    void Insert(User *a)
    {
        *qEnd=a;
        qEnd++;
        currentSize++;
    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    User* Delete()
    {
        User *temp=*qList;
        for(User **n=qList+1; n<qEnd; n++)
        {
            *(n-1)=*n;
        }
        qEnd--;
        currentSize--;
        return temp;

    }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

};



#endif // Queue_H_INCLUDED
